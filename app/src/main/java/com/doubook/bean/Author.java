package com.doubook.bean;

import org.json.*;


public class Author {
	
    private boolean isBanned;
    private boolean isSuicide;
    private String largeAvatar;
    private String id;
    private String uid;
    private String alt;
    private String avatar;
    private String type;
    private String name;
    private String url;
    
    
	public Author () {
		
	}	
        
    public Author (JSONObject json) {
    
        this.isBanned = json.optBoolean("is_banned");
        this.isSuicide = json.optBoolean("is_suicide");
        this.largeAvatar = json.optString("large_avatar");
        this.id = json.optString("id");
        this.uid = json.optString("uid");
        this.alt = json.optString("alt");
        this.avatar = json.optString("avatar");
        this.type = json.optString("type");
        this.name = json.optString("name");
        this.url = json.optString("url");

    }
    
    public boolean getIsBanned() {
        return this.isBanned;
    }

    public void setIsBanned(boolean isBanned) {
        this.isBanned = isBanned;
    }

    public boolean getIsSuicide() {
        return this.isSuicide;
    }

    public void setIsSuicide(boolean isSuicide) {
        this.isSuicide = isSuicide;
    }

    public String getLargeAvatar() {
        return this.largeAvatar;
    }

    public void setLargeAvatar(String largeAvatar) {
        this.largeAvatar = largeAvatar;
    }

    public String getId() {
        return this.id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUid() {
        return this.uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getAlt() {
        return this.alt;
    }

    public void setAlt(String alt) {
        this.alt = alt;
    }

    public String getAvatar() {
        return this.avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }

    public String getType() {
        return this.type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return this.url;
    }

    public void setUrl(String url) {
        this.url = url;
    }


    
}
